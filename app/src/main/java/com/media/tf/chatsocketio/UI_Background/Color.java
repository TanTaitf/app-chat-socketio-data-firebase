public class Color {
    String ten, color;

    public Color(String ten, String color) {
        this.ten = ten;
        this.color = color;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getColor() {
        return color;
    }
}
